  const isMenuItemActive = (item: SidebarItem) => {
    if (pathname === item.path) return true;
    if (item.children) {
      return item.children.some(child => pathname === child.path || pathname.startsWith(child.path + '/'));
    }
    return pathname.startsWith(item.path + '/');
  };

